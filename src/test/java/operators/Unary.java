package operators;

public class Unary {
	
	public static void main(String args[]){  
		int x=10;  
		System.out.println(x++);//(x=x+1)10 (11)
		System.out.println(++x);//(x=1+x) 12 
		System.out.println(x--);//(x=x-1)12 (                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                             
		System.out.println(--x);//(x=1-x)10  
		
	}}  


